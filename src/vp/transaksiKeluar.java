    protected void datatable(){
        Object[] Baris = {"id_transaksi","tgl_trx","penerima","jumlah","keperluan"};
        tabmode  = new DefaultTableModel(null, Baris);
        tabTransaksi.setModel(tabmode);
        String sql = "SELECT * FROM pengeluaran";
        try{
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while(res.next()){
                String a = res.getString("id_transaksi");
                String b = res.getString("tgl_trx");
                String c = res.getString("penerima");
                String d = res.getString("jumlah");
                String e = res.getString("keperluan");
                //String f = res.getString("id_dokter");
                
                String[] data = {a,b,c,d,e};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(transaksiKeluar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
