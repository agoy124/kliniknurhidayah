    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        String id_transaksi = idTransaksi.getText();
        java.sql.Date tgl_trans = new java.sql.Date(tglTransaksi.getDate().getTime());
        String penerima = penTransaksi.getText();
        String cash = jumTransaksi.getText();
        String keperluan = kepTransaksi.getText();
        Object[] values = {tgl_trans,penerima,cash,keperluan};
        String condition = "id_transaksi = " + id_transaksi;
        try {
            db.updateData(conn, table, columnsUpdate, values, condition);
            datatable();
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(transaksiKeluar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_editActionPerformed

