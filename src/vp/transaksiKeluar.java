    private void tabTransaksiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabTransaksiMouseClicked
        // TODO add your handling code here:
        int selectedRowIndex =  tabTransaksi.getSelectedRow();
        String a = tabmode.getValueAt(selectedRowIndex, 0).toString();
        String b = tabmode.getValueAt(selectedRowIndex, 1).toString();
        String c = tabmode.getValueAt(selectedRowIndex, 2).toString();
        String d = tabmode.getValueAt(selectedRowIndex, 3).toString();
        String e = tabmode.getValueAt(selectedRowIndex, 4).toString();

        idTransaksi.setText(a);
        tglTransaksi.setDate(java.sql.Date.valueOf(b));
        penTransaksi.setText(c);
        jumTransaksi.setText(d);
        kepTransaksi.setText(e);
    }//GEN-LAST:event_tabTransaksiMouseClicked

    private void cetakNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cetakNotaActionPerformed
        // TODO add your handling code here:
        String path =".\\src\\laporan\\buktitransaksikeluar.jasper";
        JasperReport reports; 
        Map<String, Object> parameters = new HashMap<>();
        parameters.put("id_transaksi", idTransaksi.getText());
        try {
            reports = (JasperReport) JRLoader.loadObjectFromFile(path);
            JasperPrint jprint = JasperFillManager.fillReport(path, parameters,conn);
            JasperViewer jviewer = new JasperViewer(jprint,false);
            jviewer.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
            jviewer.setVisible(true);
        } catch (JRException ex) {
            Logger.getLogger(transaksiKeluar.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cetakNotaActionPerformed

