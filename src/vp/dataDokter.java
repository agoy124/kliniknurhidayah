    protected void datatable(){
        Object[] Baris = {"ID Dokter","Nama Dokter","Tempat","Tanggal Lahir","Jenis Kelamin","Agama","Spesialis","No Telp"};
        tabmode = new DefaultTableModel(null, Baris);
        tDokter.setModel(tabmode);
        String sql = "SELECT * FROM dokter";
        try{
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String a = hasil.getString("id_dokter");
                String b = hasil.getString("nama_dokter");
                String c = hasil.getString("tempat");
                String d = hasil.getString("tgl_lahir");
                String e = hasil.getString("jenis_kelamin");
                String f = hasil.getString("agama");
                String g = hasil.getString("spesialis");
                String h = hasil.getString("no_telp");
                String[] data={a,b,c,d,e,f,g,h};
                tabmode.addRow(data);
        }
        } catch (Exception e){
            e.printStackTrace();
        }
    }
    
