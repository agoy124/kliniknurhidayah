    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        String idDokter = this.idDokter.getText();
        String nd = namaDokter.getText();
        String td = tempatDokter.getText();
        java.sql.Date tgld = new java.sql.Date(tanggalDokter.getDate().getTime());
        String jk = jkDokter.getSelectedItem().toString();
        String agd = agamaDokter.getSelectedItem().toString();
        String sp = spesialisDokter.getSelectedItem().toString();
        String telpd = telpDokter.getText();
        Object[] values = {nd,td,tgld,jk,agd,sp,telpd};
        String condition = "id_dokter = " + idDokter; //
        System.out.println(condition);
        try {
            // Memanggil fungsi updateData
            db.updateData(conn, "dokter", columnsUpdate, values, condition);
            datatable();
            reset();
        } catch (SQLException ex) {
            Logger.getLogger(dataDokter.class.getName()).log(Level.SEVERE, null, ex);
        }                  
    }//GEN-LAST:event_editActionPerformed

    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int ok = JOptionPane.showConfirmDialog(null, "hapus","Konfirmasi Dialog", JOptionPane.YES_NO_CANCEL_OPTION);
        if (ok==0){
             int id_dokter = Integer.parseInt(idDokter.getText());
             try {
                String condition = "id_dokter = " + id_dokter;
                db.deleteData(conn, "dokter", condition);
                datatable();
                reset();
            } catch (SQLException ex) {
                Logger.getLogger(dataDokter.class.getName()).log(Level.SEVERE, null, ex);

            }
        }
    }//GEN-LAST:event_deleteActionPerformed

