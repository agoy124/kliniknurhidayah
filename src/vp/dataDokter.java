    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String nd = namaDokter.getText();
        String td = tempatDokter.getText();
        java.sql.Date tgld = new java.sql.Date(tanggalDokter.getDate().getTime());
        String jk = jkDokter.getSelectedItem().toString();
        String agd = agamaDokter.getSelectedItem().toString();
        String sp = spesialisDokter.getSelectedItem().toString();
        String telpd = telpDokter.getText();
        Object[] values = {"0",nd,td,tgld,jk,agd,sp,telpd};
        
        idDokter.requestFocus();
        try{
            if(jkDokter.getSelectedIndex() == 0  || agamaDokter.getSelectedIndex() == 0 || spesialisDokter.getSelectedIndex() == 0){
                JOptionPane.showMessageDialog(null, "Data masih ada yang belum dipilih.");
            }else{
                db.insertData(conn, "dokter", columns, values);
                datatable();
            }
            
        }catch(SQLException ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_saveActionPerformed
