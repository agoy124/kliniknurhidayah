    private void cariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariActionPerformed
        java.sql.Date startDate = new java.sql.Date(dateBefore.getDate().getTime());
        java.sql.Date endDate = new java.sql.Date(dateAfter.getDate().getTime());
        //String id_transaksi = tIdTransaksi.getText();
        Object[] Baris = {"ID Transaksi","Tgl Transaksi","Debit","Credit", "Jenis Transaksi"};
        tabmode  = new DefaultTableModel(null, Baris);
        tabTransaksi.setModel(tabmode);
        String sql = "SELECT * FROM (SELECT id_transaksi, tgl_trx, total_biaya AS debit, NULL AS credit, 'Transaksi Masuk' AS jenis_transaksi\n" +
                    "FROM transaksi WHERE tgl_trx BETWEEN ? AND ?\n" +
                    "UNION ALL\n" +
                    "SELECT id_transaksi, tgl_trx,NULL AS debit, jumlah AS credit,  'Transaksi Keluar' AS jenis_transaksi\n" +
                    "FROM pengeluaran WHERE tgl_trx BETWEEN ? AND ?) AS mutasi";
        try{
            //Statement stat = conn.createStatement();
            PreparedStatement pstmt = conn.prepareStatement(sql);
            pstmt.setDate(1, startDate);
            pstmt.setDate(2, endDate);
            pstmt.setDate(3, startDate);
            pstmt.setDate(4, endDate);
            ResultSet res = pstmt.executeQuery();
            //ResultSet res = stat.executeQuery(sql);
            while(res.next()){
                String a = res.getString("id_transaksi");
                String b = res.getString("tgl_trx");
                String c = res.getString("debit");
                String d = res.getString("credit");
                String e = res.getString("jenis_transaksi");
                String[] data = {a,b,c,d,e};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(mutasi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_cariActionPerformed

