    protected void datatable(){
        Object[] Baris = {"ID Transaksi","Tgl Transaksi","jumlah", "Jenis Transaksi"};
        tabmode  = new DefaultTableModel(null, Baris);
        tabTransaksi.setModel(tabmode);
        String sql = "SELECT id_transaksi, tgl_trx, total_biaya AS jumlah, 'Transaksi Masuk' AS jenis_transaksi\n" +
                    "FROM transaksi\n" +
                    "UNION ALL\n" +
                    "SELECT id_transaksi, tgl_trx, jumlah, 'Transaksi Keluar' AS jenis_transaksi\n" +
                    "FROM pengeluaran ORDER BY tgl_trx asc";
                
        try{
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while(res.next()){
                String a = res.getString("id_transaksi");
                String b = res.getString("tgl_trx");
                String c = res.getString("jumlah");
                String d = res.getString("jenis_transaksi");
                
                String[] data = {a,b,c,d};
                tabmode.addRow(data);
            }
        } catch (SQLException ex) {
            Logger.getLogger(mutasi.class.getName()).log(Level.SEVERE, null, ex);
        }
}

